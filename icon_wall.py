class IconPanel(wx.Panel):

	def __init__(self, parent, icon_name, icon_id, icon_size, art_client, *args, **kwds):
		args = (parent, *args)
		# begin wxGlade: IconPanel.__init__
		kwds["style"] = kwds.get("style", 0) | wx.TAB_TRAVERSAL
		wx.Panel.__init__(self, *args, **kwds)
		self.icon = wx.StaticBitmap(self, wx.ID_ANY, wx.NullBitmap)
		self.caption = wx.StaticText(self, wx.ID_ANY, '')

		self.__set_properties()
		self.__do_layout()
		# end wxGlade

		self.icon_size = icon_size
		self.icon_id = icon_id
		self.art_client = art_client

		self.Refresh()
		self.caption.SetLabel(icon_name)

	def __set_properties(self):
		# begin wxGlade: IconPanel.__set_properties
		pass
		# end wxGlade

	def __do_layout(self):
		# begin wxGlade: IconPanel.__do_layout
		sizer_1 = wx.BoxSizer(wx.VERTICAL)
		sizer_1.Add(self.icon, 0, wx.ALIGN_CENTER_HORIZONTAL, 0)
		sizer_1.Add(self.caption, 0, wx.ALIGN_CENTER_HORIZONTAL, 0)
		self.SetSizer(sizer_1)
		sizer_1.Fit(self)
		self.Layout()
		# end wxGlade

	def Refresh(self, eraseBackground=True, rect=None):
		self.icon.SetBitmap(
				wx.ArtProvider.GetBitmap(self.icon_id, self.art_client, wx.Size(self.icon_size, self.icon_size))
				)
		self.Layout()
		wx.Panel.Refresh(self, eraseBackground, rect)


